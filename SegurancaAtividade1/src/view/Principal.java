    private void btnDescriptografarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDescriptografarActionPerformed
        Encryption e = new Encryption();
        String msg, msgCript, chave;
        msgCript = ctMsgCrip2.getText();
        chave = ctChave2.getText();
        if (msgCript.length() == 0 || chave.length() == 0) {
            JOptionPane.showMessageDialog(this,
                    "Mensagem ou Chave em Branco!",
                    "Mensagem ou Chave Inválida", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (msgCript.length() != chave.length()) {
            JOptionPane.showMessageDialog(this,
                    "Mensagem e Chave diferem em Tamanho!",
                    "Mensagem e Chave Inválida", JOptionPane.ERROR_MESSAGE);
            return;
        }
        msg = e.decrypt(msgCript, chave);
        ctMsg2.setText(msg);
    }//GEN-LAST:event_btnDescriptografarActionPerformed

    private void btnAtacaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtacaActionPerformed
        Encryption e = new Encryption();
        String msgCript = ctMsgCript3.getText();
        if (msgCript.length() == 0) {
            JOptionPane.showMessageDialog(this,
                    "Tamanho da Mensagem deve ser maior que zero!",
                    "Mensagem Invalida", JOptionPane.ERROR_MESSAGE);
            return;
        }
        cxtChaves.setText("");
        List<String> resultado =  ForcaBruta.ataque(msgCript.length());
        for (String chave : resultado) {
            cxtChaves.append("Chave: " + chave + " -> " + e.decrypt(msgCript, chave) + "\n");
        }
    }//GEN-LAST:event_btnAtacaActionPerformed

