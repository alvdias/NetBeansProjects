    private void btnCriptografarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCriptografarActionPerformed
        Encryption e = new Encryption();
        String msg = ctMsg.getText();
        String key, msgCript;
        if (msg.length() == 0) {
            JOptionPane.showMessageDialog(this, "Mensagem em Branco!",
                    "Mensagem Inválida", JOptionPane.ERROR_MESSAGE);
        }
        key = e.keyGenerate(msg.length());
        msgCript = e.encrypt(msg, key);
        ctMsgCript.setText(msgCript);
        ctChave.setText(key);
    }//GEN-LAST:event_btnCriptografarActionPerformed

