    public void desabilitaCampos(Container container) {
        Component components[] = container.getComponents();
        for (Component component : components) {
            if (component instanceof JFormattedTextField) {
                JFormattedTextField field = (JFormattedTextField) component;
                field.setEnabled(false);
            } else if (component instanceof JTextField) {
                JTextField field = (JTextField) component;
                field.setEnabled(false);
            } else if (component instanceof JComboBox) {
                JComboBox combo = (JComboBox) component;
                combo.setEnabled(false);
            } else if (component instanceof JTextArea) {
                JTextArea field = (JTextArea) component;
                field.setEnabled(false);
            } else if (component instanceof Container) {
                desabilitaCampos((Container) component);
            }
        }
    }

    public void habilitaCampos(Container container) {
        Component components[] = container.getComponents();
        for (Component component : components) {
            if (component instanceof JFormattedTextField) {
                JFormattedTextField field = (JFormattedTextField) component;
                field.setEnabled(true);
            } else if (component instanceof JTextField) {
                JTextField field = (JTextField) component;
                field.setEnabled(true);
            } else if (component instanceof JComboBox) {
                JComboBox combo = (JComboBox) component;
                combo.setEnabled(true);
            } else if (component instanceof JTextArea) {
                JTextArea field = (JTextArea) component;
                field.setEnabled(true);
            } else if (component instanceof JCheckBox) {
                JCheckBox field = (JCheckBox) component;
                field.setEnabled(true);
            } else if (component instanceof Container) {
                habilitaCampos((Container) component);
            }
        }
    }

    public void verificaCampos(Container conteiner) {
        Component components[] = conteiner.getComponents();
        for (Component component : components) {
            if (component instanceof JTextField) {
                JTextField field = (JTextField) component;
                field.getText().isEmpty();
                JOptionPane.showMessageDialog(null, "Ops, Algum Campo Esta Vazio!");
                field.grabFocus();
            } else if (component instanceof JFormattedTextField) {
                JFormattedTextField field = (JFormattedTextField) component;
                field.getText().isEmpty();
                JOptionPane.showMessageDialog(null, "Ops, Algum Campo Esta Vazio!");
                field.grabFocus();
            } else if (component instanceof Container) {
                verificaCampos((Container) component);
            }
        }
    }

    public void limpaCampos(Container conteiner) {
        Component components[] = conteiner.getComponents();
        for (Component component : components) {
            if (component instanceof JTextField) {
                JTextField fild = (JTextField) component;
                fild.setText("");
            } else if (component instanceof JFormattedTextField) {
                JFormattedTextField fild = (JFormattedTextField) component;
                fild.setText("");
            } else if (component instanceof Container) {
                limpaCampos((Container) component);
            }
        }
    }
