    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (flag == 1) {
            mod.setAltura(Float.parseFloat(txtAltura.getText().replaceAll(",", ".")));
            mod.setCPF(txtfCpf.getText());
            mod.setFone(txtfFone.getText().replaceAll("( )-", ""));
            mod.setIdade(Integer.parseInt(txtIdade.getText()));
            mod.setNome(txtNome.getText());
            mod.setPeso(Float.parseFloat(txtPeso.getText().replaceAll(",", ".")));
            ArrayList<Cliente> cli = new ArrayList<Cliente>();
            cli.add(mod);
            try {
                control.insereCliente(cli);
            } catch (IOException ex) {
                Logger.getLogger(FrmCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
            desabilitaCampos(this);
            btnBusca.setEnabled(true);
        } else if (flag == 2) {
            mod.setAltura(Float.parseFloat(txtAltura.getText().replaceAll(",", ".")));
            mod.setCPF(txtfCpf.getText());
            mod.setFone(txtfFone.getText().replaceAll("( )-", ""));
            mod.setIdade(Integer.parseInt(txtIdade.getText()));
            mod.setNome(txtNome.getText());
            mod.setPeso(Float.parseFloat(txtPeso.getText().replaceAll(",", ".")));
            ArrayList<Cliente> cli = new ArrayList<Cliente>();
            cli.add(mod);
            try {
                control.alteraCliente(cli);
            } catch (IOException ex) {
                Logger.getLogger(FrmCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
            desabilitaCampos(this);
            btnSalvar.setEnabled(true);
            txtfCpfBusca.setEnabled(true);

        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnBuscaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscaActionPerformed

        try {

            if (txtfCpfBusca.getText().equals("   .   .   -  ")) {
                JOptionPane.showMessageDialog(rootPane, "Digite um CPf Valido");
                txtfCpfBusca.grabFocus();
            } else {
                ArrayList<Cliente> cliente = new ArrayList<>();
                try {
                    cliente = carregaCliente();
                    if (cliente.isEmpty()) {
                        JOptionPane.showMessageDialog(rootPane, "Nenhum Registro Encontrado", "Atenção", 0);
                        txtfCpfBusca.grabFocus();
                    } else {
                        txtNome.setText(cliente.get(0).getNome());
                        txtAltura.setText(String.valueOf(cliente.get(0).getAltura()));
                        txtIdade.setText(String.valueOf(cliente.get(0).getIdade()));
                        txtPeso.setText(String.valueOf(cliente.get(0).getPeso()));
                        txtfCpf.setText(cliente.get(0).getCPF());
                        txtfFone.setText(cliente.get(0).getFone());
                    }
                } catch (IOException ex) {
                    JOptionPane.showMessageDialog(null, "Erro" + ex.getMessage());
                }
                btnSalvar.setEnabled(false);
                btnexcluir.setEnabled(true);

            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(FrmCliente.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btnBuscaActionPerformed

